    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        curp = campoCurp.getText().trim();
        nombre = campoNombre.getText().trim();
        apellidos = campoApellidos.getText().trim();
        edad = campoEdad.getText().trim();
        sexo = campoSexo.getText().trim();
        
        if(curp.equals("") || nombre.equals("") || apellidos.equals("") || edad.equals("") || sexo.equals("")) {
            System.out.println("Error");
            RegistrarReclusos.this.dispose();
            new RRFallido(RegistrarReclusos.this).show();
            borrado();
        } else {
            try {
                Connection miConexion = Conexion.dameConexion();
                Statement sentencia = miConexion.createStatement();
                
                String sqlInsertarRecluso = "INSERT INTO recluso VALUES('" + nombre +"', '" + apellidos +"', " + edad + ", '" + sexo + "', '" + curp + "', 'Recluido')";
                sentencia.executeUpdate(sqlInsertarRecluso);
                
                JOptionPane.showMessageDialog(RegistrarReclusos.this, "Recluso Registrado", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
                borrado();
            }catch(Exception e) { e.printStackTrace(); }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void borrado() {
        campoCurp.setText("");
        campoNombre.setText("");
        campoApellidos.setText("");
        campoEdad.setText("");
        campoSexo.setText("");
    }
    
