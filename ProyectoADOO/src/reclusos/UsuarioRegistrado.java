    private void actualizarUsuario(java.awt.event.ActionEvent evt) {
        nombre = campoNombre.getText().trim();
        apellido= campoApellido.getText().trim();
        edad = campoEdad.getText().trim();
        sexo = campoSexo.getText().trim();
        curp = campoCurp.getText().trim();
        domicilio = campoDomicilio.getText().trim();
        password = campoPass.getText().trim();
        tipo = (String) comboTipo.getSelectedItem();
        
        if(nombre.equals("") || apellido.equals("") || edad.equals("") || sexo.equals("") || curp.equals("") || domicilio.equals("") || password.equals("") || sexo.length() > 1 || curp.length() > 18) {
            //System.out.println("Error");
            borrado();
            UsuarioRegistrado.this.dispose();
            new ActualizacionUsuarioFallida(UsuarioRegistrado.this).show();
        } else {
            try {
                password = EncriptadorAES.encriptar(password, "secreto!");
                Connection miConexion = Conexion.dameConexion();
                Statement sentencia = miConexion.createStatement();
                
                String sqlActualizaUsuario = "UPDATE usuarios SET nombre = '" + nombre + "', apellidos = '" + apellido +"', edad = '" + edad + "', sexo = '" + sexo + "', domicilio = '" + domicilio + "', curp = '" + curp + "', contrasena = '" + password +"', tipo = '" + tipo + "' WHERE nombreusuario = '" + nombreUsuario + "'";
              
                int resp = JOptionPane.showConfirmDialog(UsuarioRegistrado.this, "Â¿Confirmar Cambios?", "Cambios", JOptionPane.YES_NO_OPTION);
                if(resp == JOptionPane.OK_OPTION) {
                     sentencia.executeUpdate(sqlActualizaUsuario);
                     JOptionPane.showMessageDialog(UsuarioRegistrado.this, "Usuario Actualizado");
                     cu.pintaDatos();
                     UsuarioRegistrado.this.dispose();
                     cu.show();
                     borrado();
                }
                miConexion.close();
            }catch(Exception e) { e.printStackTrace(); }
        }
    }
    
    private void eliminarUsuario(java.awt.event.ActionEvent evt) {
         int resp = JOptionPane.showConfirmDialog(UsuarioRegistrado.this, "Â¿Eliminar usuario?", "Eliminar", JOptionPane.YES_NO_OPTION);
       if(resp == JOptionPane.OK_OPTION) {
           try {
               Connection miConexion = Conexion.dameConexion();
               Statement sentencia = miConexion.createStatement();
               
               String sqlEliminarUsuario = "DELETE FROM usuarios WHERE nombreusuario = '" + nombreUsuario + "'";
               sentencia.executeUpdate(sqlEliminarUsuario);
               JOptionPane.showMessageDialog(UsuarioRegistrado.this, "Usuario Eliminado");
               UsuarioRegistrado.this.dispose();
               cu.pintaDatos();
               cu.show();
           } catch(Exception e) { e.printStackTrace(); }
       }
    }
    
