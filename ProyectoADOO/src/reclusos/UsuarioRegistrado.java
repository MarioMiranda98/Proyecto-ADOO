    private void colocaDatos() {
        try {
            Connection miConexion = Conexion.dameConexion();
            ResultSet rs = null;
            Statement sentencia = miConexion.createStatement();
            
            String sqlRecuperaDatos = "SELECT * FROM usuarios WHERE nombreusuario = '" + nombreUsuario +"'";
            rs = sentencia.executeQuery(sqlRecuperaDatos);
            
            while(rs.next()) {
                campoCurp.setText(rs.getString("CURP"));
                campoNombre.setText(rs.getString("nombre"));
                campoApellido.setText(rs.getString("apellidos"));
                campoEdad.setText(rs.getString("edad"));
                campoSexo.setText(rs.getString("sexo"));
                campoDomicilio.setText(rs.getString("domicilio"));
                campoUsuario.setText(rs.getString("nombreusuario"));
                campoPass.setText(EncriptadorAES.desencriptar(rs.getString("contrasena"), "secreto!"));

                String tipo = rs.getString("tipo");

                if (tipo.equalsIgnoreCase("Registrador")) {
                    comboTipo.setSelectedIndex(2);
                } else if (tipo.equalsIgnoreCase("Consultor")) {
                    comboTipo.setSelectedIndex(1);
                } else if (tipo.equalsIgnoreCase("Administrador")) {
                    comboTipo.setSelectedIndex(0);
                }
            }
            
            rs.close();
            miConexion.close();
        }catch (Exception e) { e.printStackTrace(); }
    }
    
