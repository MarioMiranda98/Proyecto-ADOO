    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        curp = campoCurp.getText();
        nombre = campoNombre.getText();
        apellidos = campoApellidos.getText();
        edad = campoEdad.getText();
        sexo = campoSexo.getText();
        domicilio = campoDomicilio.getText();
        usuario = campoUsuario.getText();
        pass = campoPass.getText();
        tipo = (String) (comboTipo.getSelectedItem());
        
        if(curp.equals("") || nombre.equals("") || apellidos.equals("") || edad.equals("") || usuario.equals("") || pass.equals("") || domicilio.equals("") || sexo.equals("") || sexo.length() > 1) {
            System.out.println("Error");
            borrado();
        } else {
            try {
                Connection miConexion = Conexion.dameConexion();
                Statement sentencia = miConexion.createStatement();
                
                pass = EncriptadorAES.encriptar(pass, "secreto!");
                String sqlInsertar = "INSERT INTO usuarios VALUES ('" + usuario + "', '" + apellidos + "', " + edad + ", '" + sexo + "', '" + domicilio + "', '" + curp + "', '" + pass + "', '" + tipo + "', '" + nombre + "')";  
                sentencia.executeUpdate(sqlInsertar);
                
                JOptionPane.showMessageDialog(RegistrarUsuario.this, "Usuario Registrado", "Exito", JOptionPane.INFORMATION_MESSAGE);
                borrado();
            } catch(Exception e) { e.printStackTrace(); }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

