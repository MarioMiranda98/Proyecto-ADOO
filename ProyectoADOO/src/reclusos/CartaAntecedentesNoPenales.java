    private void crearCarta(java.awt.event.ActionEvent evt) {
        nombre = campoNombre.getText().trim();
        apellidos = campoApellidos.getText().trim();
        curp = campoCurp.getText().trim();
        
        if(nombre.equals("") || apellidos.equals("") || curp.equals("") || curp.length() > 18) {
            //System.out.println("Error");
            borrado();
            CartaAntecedentesNoPenales.this.dispose();
            new ExpedicionCartaFallida(CartaAntecedentesNoPenales.this).show();
        } else {
            generarCartas(evt);
        }
    }
    
       private void generarCartas(java.awt.event.ActionEvent evt) {
        JFileChooser jf = new JFileChooser();
        jf.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        jf.requestFocus();
        int r = jf.showOpenDialog(CartaAntecedentesNoPenales.this);
        
        if(r == JFileChooser.APPROVE_OPTION) {
            File f = jf.getSelectedFile();
            String ruta = f.getPath();
            
            try {
                Document doc = new Document(PageSize.A4);
                PdfWriter.getInstance(doc, new FileOutputStream(ruta + "/" + curp + ".pdf"));
                doc.open();
                
                Image imagen = Image.getInstance("./src/imgs/Escudo.jpg");
                imagen.scaleAbsolute(600, 125);
                imagen.setAlignment(Element.ALIGN_CENTER);
                doc.add(imagen);
                doc.add(getCabecera(CABECERA, curp));
                doc.add(getInfo(CUERPO));
                doc.add(getFirmas(colocarFirmas()));
                doc.add(getFooter(FOOTER));
                doc.close();
                
                JOptionPane.showMessageDialog(CartaAntecedentesNoPenales.this, "Carta Guardada en " + ruta);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        borrado();
    }
 
    private Paragraph getCabecera(String texto, String curp) {
        
        texto += "\n\nC. " + nombre + " " + apellidos + "\n\n"
                + "Con el siguiente CURP \n\n" + curp +"\n\n";  
            
        Paragraph p = new Paragraph();
        Chunk c = new Chunk();
        p.setAlignment(Element.ALIGN_CENTER);
        c.append(texto);
        c.setFont(fuenteBold);
        p.add(c);
        
        return p;
    }
    
      private Paragraph getInfo(String texto) {
        Paragraph p = new Paragraph();
        Chunk c = new Chunk();
        p.setAlignment(Element.ALIGN_CENTER);
        c.append(texto);
        c.setFont(fuente);
        p.add(c);
        
        return p;
    }
      
      private Paragraph getFooter(String texto) {
        Paragraph p = new Paragraph();
        Chunk c = new Chunk();
        p.setAlignment(Element.ALIGN_RIGHT);
        c.append(texto);
        c.setFont(fuenteItalica);
        p.add(c);
        
        return p;
    }
    
    private Paragraph getFirmas(String texto) {
        Paragraph p = new Paragraph();
        Chunk c = new Chunk();
        p.setAlignment(Element.ALIGN_CENTER);
        c.append(texto);
        c.setFont(fuente);
        p.add(c);
        
        return p;
    }  

     private String colocarFirmas() {
        String formato = "\n\n"
                + "_________________________________________________"
                + "\n\n"
                + "Director General del Reclusorio";
     
        return formato;
    }
     
